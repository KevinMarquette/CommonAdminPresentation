# Scheduled Scripts
<#
The important thing to remember when setting up a scheduled 
task is that the application is powershell.exe. You script 
needs to be set as a -file argument to the script. You 
need to use the full path and include double quotes if 
there are spaces in the path or name.
#>

# Program/Script: 
 Powershell.exe

# Add arguments (optional): 
 -File "c:\MyScripts\script.ps1" 
 

